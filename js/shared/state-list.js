/**
 * @returns {{name: string, code: string}[]}
 */
const getAllStates = () => {
    return STATE_LIST.map(el => {
        return {
            name: el.stateName,
            code: el.stateCode
        };
    });
};

/**
 * @param {string} stateCode 
 * @returns {string[]}
 */
const getAreaCodesByStateCode = (stateCode) => {
    return STATE_LIST
        .find(el => el.stateCode === stateCode)
        .areaCodes;
};

/**
 * @returns {string[]}
 */
const getAllAreaCodes = () => {
    return STATE_LIST
        .flatMap(el => el.areaCodes)
        .sort();
};